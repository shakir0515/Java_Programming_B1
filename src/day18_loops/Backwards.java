package day18_loops;

public class Backwards {
    public static void main(String[] args) {

        for (int i = 0; i <= 20; i++) {
            System.out.println(i);
        }

        System.out.println("------------");
        for (int i = 20; i >= 0 ; i--) {
            System.out.println(i);
        }




    }
}
