package day37_a_abstraction.car;

public abstract class Car {

    // abstract method has to be in abstract class
    // you cannot have abstract method in non //abstract class
public abstract void start();


}
/*
*Abstract method does not have body
* Abstract method tell what it does but now how
* it has to be in abstract class
* whatever class extends it has to implement abstract class
* ABstract class cannot be instantiated that means cannot create an object*/